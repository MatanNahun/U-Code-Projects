class Player {
  firstName: string;
  lastName: string;
  jerseyNumber: string;
  position: string;

  constructor(
    firstName: string,
    lastName: string,
    jerseyNumber: string,
    position: string
  ) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.jerseyNumber = jerseyNumber;
    this.position = position;
  }
}
